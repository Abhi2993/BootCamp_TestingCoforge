package Task53AzureAssignment;



public class UnaryOperators {

	public static void main(String[] args) {

		boolean employeeActive = false;

		System.out.println("Employee Active : " + employeeActive);

		System.out.println("Using NOT Operator : " + (!employeeActive));

		int employeeId = 10;

		System.out.println("Employee ID : " + employeeId);

		employeeId++;

		System.out.println("Increment ID : " + employeeId);

		employeeId--;

		System.out.println("Decrement ID : " + employeeId);

	}
}